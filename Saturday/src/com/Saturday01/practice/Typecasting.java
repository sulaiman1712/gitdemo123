package com.Saturday01.practice;

public class Typecasting 
{

	public static void main(String[] args) 
	{
		byte a=1;
		
		int b=a;
		System.out.println("Value of b is:" +b);
		
		int c=10;
		
		short d=(short) c;
		byte r=(byte) d;
		System.out.println("Value of d is:"+c);
		System.out.println("Value of r is:"+r);
		

	}

}
